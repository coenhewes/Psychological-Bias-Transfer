def all_prompts():
    """Yields (category, prompt) tuples across the full 200-prompt set."""
    for category, prompts in CATEGORIES.items():
        for p in prompts:
            yield category, p


if __name__ == "__main__":
    counts = {cat: len(prompts) for cat, prompts in CATEGORIES.items()}
    total = sum(counts.values())
    print("Category counts:", counts)
    print("Total prompts:", total)
    assert total == 200, f"expected 200 prompts, got {total}"
    for cat, prompts in CATEGORIES.items():
        assert len(prompts) == 40, f"{cat} has {len(prompts)} prompts, expected 40"
        assert len(set(prompts)) == 40, f"{cat} has duplicate prompts"
    print("OK: 5 categories x 40 prompts, all unique within category.")
